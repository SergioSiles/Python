def traductor():
    frase = "Hola Mundo"
    frase = frase.upper()
    resultado = ""
    for letra in frase:
        if letra == " ":
            resultado += " / "
        elif letra in diccionario:
            resultado += diccionario[letra] + " "
        else:
            resultado += "? "
    print(resultado)
